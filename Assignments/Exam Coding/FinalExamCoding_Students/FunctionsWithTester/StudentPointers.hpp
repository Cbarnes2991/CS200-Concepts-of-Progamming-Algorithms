#ifndef _STUDENT_POINTERS_HPP
#define _STUDENT_POINTERS_HPP

/*
Function: GetAddress
Return type: integer pointer
Input: an integer reference

Return the address of the input variable.
*/

/*
Function: GetValue
Return type: integer
Input: an integer pointer

Using the input pointer variable,
return the value that it is pointing at.
*/

/*
Function: CheckIfNull
Return type: Boolean
Input: integer pointer

Check to see if the pointer is null.
If it is, return true. Otherwise, return false.
*/

/*
Function: CompareAddresses
Return type: Boolean
Input: Two integer pointers

If the two pointers are pointing to the same
address, return true. Otherwise, return false.
*/

/*
Function: CompareValues
Return type: Boolean
Input: Two integer pointers

For both pointers, look at their values
that they are pointing to. If the values
are equal, return true. Otherwise,
return false.
*/

#endif